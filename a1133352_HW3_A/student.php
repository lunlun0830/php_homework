<?php

// --- 第一部分：條件式輸出姓名 ---
$myName = "蔡勝綸"; // 請在這裡換成你的名字
$isNameVisible = true;  // 設定為 true 顯示姓名，設定為 false 則不顯示

if ($isNameVisible) {
    echo "你好，我的姓名是：" . $myName . "<br>";
} else {
    echo "目前設定為不顯示姓名。<br>";
}

// 換行分隔
echo "<hr>";

// --- 第二部分：使用 $i 和 $j 輸出現在與 10 年後的年紀 ---
$i = 19;       // $i 代表現在的年紀，請改成你的真實年齡
$j = $i + 10;  // $j 代表 10 年後的年紀

echo "我現在的年紀是：" . $i . " 歲<br>";
echo "10 年後的年紀是：" . $j . " 歲<br>";

?>